#!/usr/bin/env node
// gen-rom-font.js — bake our "extra" bitmap fonts into the shared atlas layout.
//
// Two sources feed the same atlas format (so the one LoadFontFromImage(YELLOW, 0)
// loader in studio.c handles them all, no new runtime code per font):
//
//   1. IBM ROM dumps  (tools/fonts/*.F08) — raw 256-glyph × H-byte bitmaps from
//      VileR's int10h collection (github.com/viler-int10h/vga-text-mode-fonts).
//      Each byte = one 8-pixel scanline, MSB = leftmost. romGlyphs() takes a cell
//      width: 8 for true CGA (FONT_THIN), 9 for MDA/VGA (the 9th column copies the
//      8th over the 0xC0–0xDF box-drawing range so horizontal rules connect).
//
//   2. TrueType rasterization — ttfGlyphs() bakes a monospace TTF (FONT_COMIC,
//      from ComicMono-Bold.ttf) into fixed cells via font-bake.js's outline path,
//      with a per-glyph patch table for hand cleanups that survive re-bakes.
//
// Atlas contract (matches dos_8x8 exactly): 16×16 grid, firstChar 0 (full OEM /
// CP437 set), 1px opaque-yellow separators + border (the key colour), glyph
// pixels opaque white, cell interiors transparent.
//
//   node tools/gen-rom-font.js     → regenerates every font below

const fs   = require('fs')
const zlib = require('zlib')
const path = require('path')
const opentype = require('./vendor/opentype.cjs')
const { contoursFromPath, rasterize } = require('./font-bake.js')

const ROOT = path.join(__dirname, '..')
const COLS = 16, ROWS = 16
const KEY  = [255, 255, 0, 255]   // separator / key colour
const INK  = [255, 255, 255, 255] // glyph pixel
const VOID = [0, 0, 0, 0]         // cell interior — transparent (invisible, but != key)

// ── PNG encode (RGBA / colortype 6) ──
function crc32(buf) {
  let c = ~0
  for (let i = 0; i < buf.length; i++) { c ^= buf[i]; for (let k = 0; k < 8; k++) c = (c >>> 1) ^ (0xEDB88320 & -(c & 1)) }
  return ~c
}
function makeChunk(type, data) {
  const len = Buffer.alloc(4); len.writeUInt32BE(data.length)
  const tb = Buffer.from(type, 'ascii')
  const crc = Buffer.alloc(4); crc.writeUInt32BE(crc32(Buffer.concat([tb, data])) >>> 0)
  return Buffer.concat([len, tb, data, crc])
}
// glyphs: array of 256 grids; each grid is gh rows of gw 0/1 values
function writeAtlas(glyphs, gw, gh, pngName, hdrName, sym, note) {
  const W = COLS * gw + (COLS + 1), H = ROWS * gh + (ROWS + 1)
  const px = new Uint8Array(W * H * 4)
  for (let i = 0; i < W * H; i++) px.set(KEY, i * 4)
  for (let code = 0; code < 256; code++) {
    const g = glyphs[code]
    const x0 = 1 + (code % COLS) * (gw + 1), y0 = 1 + ((code / COLS) | 0) * (gh + 1)
    for (let y = 0; y < gh; y++) for (let x = 0; x < gw; x++)
      px.set(g[y][x] ? INK : VOID, ((y0 + y) * W + (x0 + x)) * 4)
  }
  const scan = Buffer.alloc(H * (1 + W * 4))
  for (let y = 0; y < H; y++) {
    scan[y * (1 + W * 4)] = 0
    for (let x = 0; x < W; x++) { const s = (y * W + x) * 4, d = y * (1 + W * 4) + 1 + x * 4
      scan[d] = px[s]; scan[d+1] = px[s+1]; scan[d+2] = px[s+2]; scan[d+3] = px[s+3] }
  }
  const ihdr = Buffer.alloc(13)
  ihdr.writeUInt32BE(W, 0); ihdr.writeUInt32BE(H, 4); ihdr[8] = 8; ihdr[9] = 6
  const png = Buffer.concat([
    Buffer.from([0x89,0x50,0x4e,0x47,0x0d,0x0a,0x1a,0x0a]),
    makeChunk('IHDR', ihdr), makeChunk('IDAT', zlib.deflateSync(scan)), makeChunk('IEND', Buffer.alloc(0)),
  ])
  fs.writeFileSync(path.join(ROOT, 'editor/public', pngName), png)
  let h = '// Auto-generated by tools/gen-rom-font.js — do not edit by hand.\n'
  h += `// ${note}\n`
  h += `static const unsigned char ${sym}_DATA[] = {\n`
  for (let i = 0; i < png.length; i += 12)
    h += '  ' + [...png.slice(i, i + 12)].map(b => '0x' + b.toString(16).padStart(2, '0')).join(', ') + ',\n'
  h += `};\nstatic const unsigned int ${sym}_DATA_LEN = ${png.length};\n`
  fs.writeFileSync(path.join(ROOT, 'runtime', hdrName), h)
  console.log(`${pngName.padEnd(16)} (${W}×${H}), runtime/${hdrName} (${png.length}B)`)
}

// ── source 1: IBM ROM dump → 9-wide glyph grids ──
const NINTH_LO = 0xC0, NINTH_HI = 0xDF
// width 9 = IBM MDA/VGA cell (8 pixels + the box-drawing 9th column); width 8 =
// true CGA cell (no 9th column, the chip rendered glyphs edge-to-edge at 8px).
function romGlyphs(srcFile, rows, width = 9) {
  const rom = fs.readFileSync(path.join(ROOT, 'tools/fonts', srcFile))
  if (rom.length !== 256 * rows) { console.error(`${srcFile}: expected ${256 * rows} bytes, got ${rom.length}`); process.exit(1) }
  const glyphs = []
  for (let code = 0; code < 256; code++) {
    const g = []
    for (let y = 0; y < rows; y++) {
      const row = rom[code * rows + y], r = []
      for (let x = 0; x < width; x++) {
        if (x < 8) r.push((row >> (7 - x)) & 1)
        else r.push((code >= NINTH_LO && code <= NINTH_HI) ? (row & 1) : 0)  // 9th col copies col 8
      }
      g.push(r)
    }
    glyphs.push(g)
  }
  return glyphs
}

// ── source 2: rasterize a TTF into fixed gw×gh cells (printable ASCII 32–127) ──
// Reuses font-bake.js's outline→coverage path. Monospace glyphs are centred in
// the cell by advance width; the baseline is placed so descenders clear the
// bottom edge. Non-printable codes (0–31, 128–255) stay blank — this atlas loads
// with firstChar 0 like the ROM fonts, so cell index == codepoint.
// Hand cleanups applied AFTER rasterization, so they survive every re-bake.
// Keyed by character → list of [x, y, value] (value 1 = set ink, 0 = clear).
// Coordinates are cell-local (0..gw-1, 0..gh-1; y down). Add entries as the
// auto-rasterized glyph needs a stray pixel removed or a gap filled.
const COMIC_PATCHES = {
  // '&': [[10, 3, 0]],   // example: clear a stray pixel top-right of the ampersand
}

function ttfGlyphs(fontFile, px, gw, gh, threshold = 0.5, patches = {}) {
  const buf  = fs.readFileSync(path.join(ROOT, 'editor/public', fontFile))
  const font = opentype.parse(buf.buffer.slice(buf.byteOffset, buf.byteOffset + buf.byteLength))
  const scale = px / font.unitsPerEm
  const advPx = font.charToGlyph('M').advanceWidth * scale   // monospace: same for all
  const ox    = (gw - advPx) / 2                              // centre the advance box in the cell
  const by    = Math.round(gh + font.descender * scale)       // baseline: leave room for descenders
  const glyphs = []
  for (let code = 0; code < 256; code++) {
    const g = Array.from({ length: gh }, () => Array(gw).fill(0))
    if (code >= 32 && code <= 127) {
      const p = font.getPath(String.fromCharCode(code), ox, by, px)
      const cov = rasterize(contoursFromPath(p), gw, gh)
      for (let y = 0; y < gh; y++) for (let x = 0; x < gw; x++)
        if (cov[y * gw + x] >= threshold) g[y][x] = 1
      const fixes = patches[String.fromCharCode(code)]
      if (fixes) for (const [x, y, v] of fixes) if (y >= 0 && y < gh && x >= 0 && x < gw) g[y][x] = v
    }
    glyphs.push(g)
  }
  return glyphs
}

// ── generate everything ──
writeAtlas(ttfGlyphs('ComicMono-Bold.ttf', 18, 10, 20, 0.5, COMIC_PATCHES), 10, 20, 'fontcomic10x20.png', 'fontcomic10x20_data.h', 'FONTCOMIC10X20',
  'Comic Mono Bold rasterized at 18px into 10×20 cells (source: ComicMono-Bold.ttf, MIT).')
writeAtlas(romGlyphs('CGA-TH.F08', 8, 8), 8, 8, 'fontthin8x8.png', 'fontthin8x8_data.h', 'FONTTHIN8X8',
  'IBM CGA "thin" 8×8 atlas — the narrow alternate CGA ROM font (source: int10h CGA-TH.F08, public-domain ROM dump).')
